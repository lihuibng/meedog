    private static String buildUrl(String host, String path, Map<String, String> querys) throws UnsupportedEncodingException {
        StringBuilder sbUrl = new StringBuilder();
        sbUrl.append(host);
        if (!StringUtils.isBlank(path)) {
            sbUrl.append(path);
        }
        if (null != querys) {
            StringBuilder sbQuery = new StringBuilder();
            for (Map.Entry<String, String> query : querys.entrySet()) {
                if (0 < sbQuery.length()) {
                    sbQuery.append("&");
                }
                if (StringUtils.isBlank(query.getKey()) && !StringUtils.isBlank(query.getValue())) {
                    sbQuery.append(query.getValue());
                }
                if (!StringUtils.isBlank(query.getKey())) {
                    sbQuery.append(query.getKey());
                    if (!StringUtils.isBlank(query.getValue())) {
                        sbQuery.append("=");
                        sbQuery.append(URLEncoder.encode(query.getValue(), "utf-8"));
                    }
                }
            }
            if (0 < sbQuery.length()) {
                sbUrl.append("?").append(sbQuery);
            }
        }

        return sbUrl.toString();
    }

    private static HttpClient wrapClient(String host) {
        HttpClient httpClient = HttpClientBuilder.create().build();
        if (host.startsWith("https://")) {
            return sslClient();
        }

        return httpClient;
    }

    private static CloseableHttpClient sslClient() {
        //Creating SSLContextBuilder object
        SSLContextBuilder SSLBuilder = SSLContexts.custom();

        //Loading the Keystore file
        File file = new File("mykeystore.jks");
        try {
            SSLBuilder = SSLBuilder.loadTrustMaterial(file,
                    "meedog_1".toCharArray());
            //Building the SSLContext usiong the build() method
            SSLContext sslcontext = null;
            try {
                sslcontext = SSLBuilder.build();
                //Creating SSLConnectionSocketFactory object
                //SSLConnectionSocketFactory is a layered socket factory for TSL and SSL connections.
                // Using this, you can verify the Https server using a list of trusted certificates
                // and authenticate the given Https server. You can create this in many ways.
                // Depending on the way you create an SSLConnectionSocketFactory object,
                // you can allow all hosts, allow only self-signed certificates, allow only particular protocols, etc.
                // To allow only particular protocols, create SSLConnectionSocketFactory object by passing an SSLContext object,
                // string array representing the protocols need to be supported, string array
                // representing the cipher suits need to be supportedand a HostnameVerifier object to its constructor.
                SSLConnectionSocketFactory sslConSocFactory = new SSLConnectionSocketFactory(sslcontext,
                        new String[]{"TLSv1"},
                        null,
                        SSLConnectionSocketFactory.getDefaultHostnameVerifier());
                //To allow all hosts, create SSLConnectionSocketFactory object
                // by passing a SSLContext object and a NoopHostnameVerifier object
                // sslConSocFactory = new SSLConnectionSocketFactory(sslcontext, new NoopHostnameVerifier());

                //Creating HttpClientBuilder
                HttpClientBuilder clientbuilder = HttpClients.custom();

                //Setting the SSLConnectionSocketFactory
                clientbuilder = clientbuilder.setSSLSocketFactory(sslConSocFactory);

                //Building the CloseableHttpClient
                CloseableHttpClient httpclient = clientbuilder.build();
                return httpclient;
            } catch (KeyManagementException e) {
                e.printStackTrace();
            }
        } catch (NoSuchAlgorithmException e) {
            e.printStackTrace();
        } catch (KeyStoreException e) {
            e.printStackTrace();
        } catch (CertificateException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        }
        return null;
    }

    public String doPostFin(String url, Map<String,String> map, String charset){
        CloseableHttpClient httpClient = null;
        HttpPost httpPost = null;
        String result = null;
        try{
            httpClient = HttpClients.createDefault();
            httpPost = new HttpPost(url);
            //设置参数
            List<NameValuePair> list = new ArrayList<NameValuePair>();
            Iterator iterator = map.entrySet().iterator();
            while(iterator.hasNext()){
                Map.Entry<String,String> elem = (Map.Entry<String, String>) iterator.next();
                list.add(new BasicNameValuePair(elem.getKey(),elem.getValue()));
            }
            if(list.size() > 0){
                UrlEncodedFormEntity entity = new UrlEncodedFormEntity(list,charset);
                httpPost.setEntity(entity);
            }
            HttpResponse response = httpClient.execute(httpPost);
            if(response != null){
                HttpEntity resEntity = response.getEntity();
                if(resEntity != null){
                    result = EntityUtils.toString(resEntity,charset);
                }
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
        return result;
    }

